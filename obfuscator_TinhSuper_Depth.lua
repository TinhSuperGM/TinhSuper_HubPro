-- ====== NOISE (rất nhiều) ======
-- Các khối dưới đây là "noise" thực thi nhưng KHÔNG ảnh hưởng logic thật.
local function _build_big_table(N)
    local t = {}
    for i = 1, (N or 1024) do
        -- tạo chuỗi dài biến đổi
        local len = (i % 97) + 16
        t[i] = string.rep("A", len) .. tostring(i) .. string.rep("B", (i%31)+3)
    end
    return t
end

local __NOISE_TABLE__ = _build_big_table(2048)

local function _fake_loop_activity()
    -- vòng lặp nhẹ nhàng, không ảnh hưởng
    for i = 1, 8 do
        local s = __NOISE_TABLE__[(i * 13) % #__NOISE_TABLE__ + 1]
        -- thực hiện các phép toán thuần tuý
        local _v = (#s * i + 7) % 100
        if _v == 999 then
            -- không bao giờ xảy ra, chỉ để confuse
            _v = _v
        end
        task.wait(0.001)
    end
end

-- spawn noise tasks nhưng không in ra gì
pcall(function()
    task.spawn(function()
        _fake_loop_activity()
    end)
end)

-- nhiều hàm fake để làm rối
local function fake_decrypt(a)
    -- giả decrypt: trả lại chuỗi đã reverse nhưng không gọi API
    if type(a) ~= "string" then return "" end
    local b = {}
    for i = #a, 1, -1 do
        b[#b+1] = a:sub(i,i)
    end
    return table.concat(b)
end

local function fake_network_check()
    -- fake kiểm tra mạng, không dùng HttpGet
    local ok = true
    local ping = (math.floor(tick()) % 100) + 1
    return ok, ping
end

local function fake_ui_label(text)
    -- fake UI builder: tạo table mô phỏng label
    return { type = "Label", text = tostring(text or "") }
end

