-- ====== FAKE LOGIC (~4%) =====
-- giả decrypt / network / UI, KHÔNG gọi API thật
local function fake_decrypt_flow(x)
    local out = fake_decrypt(tostring(x))
    task.wait(0.002)
    return out
end

local function fake_network_flow()
    local ok, p = fake_network_check()
    if not ok then return false end
    local hdr = {user = "anon", ttl = 60, ping = p}
    return true, hdr
end

local function fake_dispatch()
    local ok, hdr = fake_network_flow()
    if ok then
        local lbl = fake_ui_label("initialized")
        return {status = true, label = lbl, hdr = hdr}
    end
    return {status = false}
end

pcall(function()
    task.spawn(function()
        local _ = fake_dispatch()
        local __ = fake_decrypt_flow(__BIG_NOISE_STR__)
    end)
end)

